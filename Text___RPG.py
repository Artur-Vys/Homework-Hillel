import random  # Подключаем "модуль случайности" (для кубика)

def game():
    # --- 1. НАСТРОЙКА ГЕРОЯ ---
    print("Привет, Сын Сюжета! В мире разруха.")
    print("У тебя есть 10 очков.")
    
    # Бесконечный цикл, пока игрок не введет нормальное число
    while True:
        try:
            # Спрашиваем силу
            strength = int(input("Сколько очков кинуть в СИЛУ (урон)? Остальное пойдет в ЗДОРОВЬЕ: "))
            
            if 0 <= strength <= 10:
                # Математика распределения
                hp_points = 10 - strength   # Всё, что не сила — это здоровье
                
                player_hp = hp_points * 10  # Пусть 1 очко дает 10 жизней
                player_dmg = strength       # А сила напрямую влияет на урон
                
                print(f"Готово! Твое здоровье: {player_hp}, Твоя сила: {player_dmg}")
                break # Выходим из цикла настройки, идем играть
            else:
                print("Эй, у тебя всего 10 очков! Введи число от 0 до 10.")
        except ValueError:
            print("Нужно вводить цифры!")

    # --- 2. ФУНКЦИЯ БИТВЫ (ШАБЛОН) ---
    # Мы создаем "инструкцию", как драться, чтобы не писать одно и то же для каждого монстра
    def fight(enemy_name, enemy_hp, enemy_dmg):
        nonlocal player_hp # Говорим функции, что используем здоровье игрока из внешней части
        
        print(f"\n--- НАПАДАЕТ {enemy_name}! (HP: {enemy_hp}) ---")
        
        # Пока никто не умер (HP у обоих больше 0)
        while player_hp > 0 and enemy_hp > 0:
            input(f"Нажми Enter, чтобы ударить {enemy_name}...")
            
            # --- ТВОЯ МЕХАНИКА КУБИКА ---
            dice = random.randint(1, 20) # Кидаем кубик от 1 до 20
            print(f"На кубике выпало: {dice}")
            
            damage = 0
            
            if dice >= 18: # КРИТ (18, 19, 20)
                print("КРИТ!!! ТЫ СНОСИШЬ ЕМУ ГОЛОВУ!")
                damage = 999 # Убиваем сразу
            elif dice <= 4: # Неудача (1, 2, 3, 4)
                print("Ты промахнулся...")
                damage = 0
            else: # Обычный удар
                print("Ты попал!")
                damage = player_dmg + random.randint(1, 3) # Твоя сила + немного рандома
            
            # Отнимаем жизни у врага
            enemy_hp -= damage
            print(f"Ты нанес {damage} урона.")
            
            if enemy_hp <= 0:
                print(f"{enemy_name} погиб!")
                return True # Победа
            
            # Ход врага
            print(f"{enemy_name} кусает тебя!")
            player_hp -= enemy_dmg
            print(f"У тебя осталось {player_hp} жизней.")
            
            if player_hp <= 0:
                print("Ты умер... Конец игры.")
                return False # Проигрыш

    # --- 3. СЮЖЕТ И МОНСТРЫ ---
    # Битва 1
    print("\nТы идешь по руинам и видишь крысу.")
    if fight("Крыса", 20, 2) == False: return # Если проиграл — выходим из игры
    
    # Битва 2
    print("\nТы подходишь к замку зла.")
    if fight("Орк", 50, 5) == False: return
    
    print("\nТЫ СПАС МИР! (Потому что ты сын сюжета)")

# Запуск игры
game()